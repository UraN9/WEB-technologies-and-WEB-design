// 1-2. Оголошення змінних
let x = 50;
let y = 10;

// 3. Виведення суми
console.log("Сума x + y:", x + y);

// 4. Множення
let d = x * y;
console.log("Значення d = x * y:", d);

// 5. Оголошення змінних з іменем, прізвищем та віком
let firstName = "Дмитро", lastName = "Кшивецький", age = 18;

// 6. Арифметичні операції з присвоєнням
d /= y;   // d = d / y
x -= d;   // x = x - d
age += x; // age = age + x

// 7. Виведення результату в одному рядку
console.log(firstName + " " + lastName + " " + age);
